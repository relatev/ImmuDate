package Date.Chronologies;

public class ChronologicalZero {

    public final long YEAR_ZERO;
    public final int MONTH_ZERO;
    public final int WEEK_ZERO;
    public final int DAY_ZERO;

    ChronologicalZero(long yearZero, int monthZero, int weekZero, int dayZero) {
        this.YEAR_ZERO = yearZero;
        this.MONTH_ZERO = monthZero;
        this.WEEK_ZERO = weekZero;
        this.DAY_ZERO = dayZero;
    }

}
